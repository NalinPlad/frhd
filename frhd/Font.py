import copy

def shift(char, x, y):
    letter = copy.deepcopy(char)
    for ind_l, line in enumerate(letter):
        for ind,point in enumerate(line):
            if ind % 2 == 0:
                letter[ind_l][ind] += x
            else:
                letter[ind_l][ind] += y
    return letter
            

